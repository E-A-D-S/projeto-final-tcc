    <h4>Sobre nós: SISAM e Clínica Escola:</h4>

      <p>Clínica Escola:</p> 
      <p>A Clínica-escola é um espaço de atendimento psicológico realizado por alunos formandos do Curso de Psicologia, sob a supervisão de professores do curso. Tem como público-alvo crianças, adolescentes e adultos e fica localizada na Ulbra de São Jerônimo. 
      O Serviço foi criado no Ano de 2010. Os atendimentos eram, inicialmente, no CAPS I da cidade, mediante convênio da Universidade com a Prefeitura Local. Em Agosto de 2019, foi disponibilizado pela Ulbra um espaço dentro da própria Universidade para a realização das atividades e atendimentos da Clínica Escola.
      Esses atendimentos são realizados por estagiários em reta final do curso, supervisionados por professores capacitados, que dão suporte e orientação para todos os atendimentos e os atendimentos são totalmente gratuitos.
      Horário de funcionamento: segunda a sexta-feira, das 13 às 22h.</p> 

      <p>SISAM:</p> 
      <p>O SISAM está ligado ao Serviço Escola de Psicologia e foi criado no ano de 2019, com o intuito de planejar e executar projetos na comunidade da Região Carbonífera.
      O Serviço de Intervenção em Saúde Mental é voltado para prestar auxílio na Prevenção e Promoção da Saúde Mental nas instituições, de acordo com a demanda e necessidade.
      A criação do SISAM se deu a partir da percepção do déficit de profissionais de Psicologia nas instituições da região. A intenção é oferencer apoio conforme às demandas apresentadas, realizando trabalhos em grupos, visitas, palestras, campanhas, etc. 
